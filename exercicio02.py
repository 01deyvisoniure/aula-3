n1=float(input("digite o primeiro numero: "))
escolha=str(input("escolha: "))
n2=float(input("digite o segundo numero: "))

if escolha =="+":
    soma = n1 + n2
    print(soma)
elif escolha =="-":
    sub = n1 - n2
    print (sub)
elif escolha =="/":
    divisao = n2 / n1
    print(divisao)
elif escolha =="X":
    mult = n1 * n2
    print(mult)



